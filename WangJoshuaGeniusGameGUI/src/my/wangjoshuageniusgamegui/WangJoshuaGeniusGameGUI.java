    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    //try statement
    try{
        //if the game isn't being played, and the playAgain button is true
        if(gamePlaying == false && playAgainCounter == true){
            //makes numLetters the text field by using parseInt
            numLetters = Integer.parseInt(jTextField2.getText());
            //if numLetters is not less than 3 or greater than 10
            if(numLetters<=10 && numLetters >= 3){
                //for loop that is based off of numLetters
                for(int i = 0; i<numLetters ; i++){
                    //creates a random number using random
                    randomChar = (int) (Math.random()*3)+1;
                    //if the random number is 1
                    if(randomChar == 1){
                        //sets secret word char as r
                        secretWord[i] = 'r';
                        //puts letter in display(for testing, shows combination)
                        display = display + "\nr";
                        jTextArea1.setText(display);
                    //if random number is 2
                    }else if(randomChar == 2){
                        //sets secret word char as g
                        secretWord[i] = 'g';
                        //puts letter in display(for testing, shows combination)
                        display = display + "\ng";
                        //prints out display
                        jTextArea1.setText(display);
                    }else if(randomChar == 3){
                        //sets secret word char as b
                        secretWord[i] = 'b';
                        //puts letter in display(for testing, shows combination)
                        display = display + "\nb";
                        //prints out displays
                        jTextArea1.setText(display);
                    }
                }
                //uses for loop based off numLetters
                for(int i = 0;i<numLetters;i++){
                    //sets introLetters up with | and userGuess array
                    introLetters = introLetters +"| " + usersGuess[i] + " ";
                }
                //puts space behind introLetters
                introLetters = "                                                   " 
                    + "                                   " +introLetters + "|";
                //sets displays with all the new round information
                display = display + "\n" + introLetters + "\nRound: " + roundCounter 
                    + "   Please guess " + numLetters + " characters rgb: ";
                //prints out display
                jTextArea1.setText(display);
                //clears the text field
                jTextField2.setText("");
                //clears the error message
                jLabel4.setText("");
                //sets gamePlaying as true because you are now playing
                gamePlaying = true;
                //you can't playAgain
                playAgainCounter = false;
            //if the number isn't between 3-10
            }else{
                //displays error message
                jLabel4.setText("Please enter a number between 3-10 inclusive");
                //clears field
                jTextField2.setText("");
            }
        }
    //catch statement
    }catch(NumberFormatException e){
        //displays error message
        jLabel4.setText("Please enter a number between 3-10 inclusive");
        //clears field
        jTextField2.setText("");
    }
    }//GEN-LAST:event_jButton3ActionPerformed

