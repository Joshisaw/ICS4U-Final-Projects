    public static int checkGuess(char[]secretWord, char[]usersGuess, 
            int numLetters){
        int correctCounter = 0;
        //for loop that goes through the entire array
        for(int i = 0; i<numLetters;i++){
            //compares secretWord with your guess
            if(secretWord[i]==usersGuess[i]){
                //adds one to correctCounter
                correctCounter++;
            }
        }
        //returns correctCounter
        return correctCounter;
    }

