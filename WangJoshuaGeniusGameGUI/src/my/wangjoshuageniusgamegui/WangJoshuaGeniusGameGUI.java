    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //if the game is being played
        if(gamePlaying == true){
            //resets values for a new round
            properGuess = true;
            //try statement
            try{
            //gets users guess
            userGuessString = jTextField1.getText();
                for(int i = 0; i<numLetters;i++){
                    //puts all of the chars into the array
                    usersGuess[i] = userGuessString.charAt(i);
                    //if the guess is r,g or b
                    if(usersGuess[i]=='r'||usersGuess[i]=='g'||usersGuess[i]=='b'){
                        //nothing happens
                    }else{
                        //but if guess isn't r,g or b, proper guess is false
                        properGuess = false;
                    }
                }
            //if guess is r,g or b and the length is equal to numLetters
            if(properGuess==true && userGuessString.length() == numLetters){
                //finds the amount of correct guesses
                correctCounter = checkGuess(secretWord,usersGuess,numLetters);
                //finds wrong counter with math
                wrongCounter = numLetters-correctCounter;
                //puts the guess in display
                display = display + userGuessString;
                //resets introLetters for new round
                introLetters = "";
                //for loop to numLetters
                for(int i = 0;i<numLetters;i++){
                    //inputs the array within the ||||
                    introLetters = introLetters +"| " + usersGuess[i] + " ";
                }
                //puts a space behind the |||| for the console effect
                introLetters = "                                               " 
                + "                                       " +introLetters + "|";
                //if correct counter isn't numLetters
                if(correctCounter != numLetters){
                    //increases roundCounter by 1
                    roundCounter++;
                    //put all of the new round statements into display string
                    display = display + "\n" + introLetters + "    " + correctCounter 
                    + " correct, " + wrongCounter + " incorrect" + "\nRound: " 
                    + roundCounter + "   Please guess " + numLetters 
                    + " characters rgb: ";
                    //clears the textField
                    jTextField1.setText("");
                    //prints out display
                    jTextArea1.setText(display);
                    //clears the label
                    jLabel5.setText("");
                //but if correctCounter is numLetters, which means you won
                }else if(correctCounter == numLetters){
                    //clears textField
                    jTextField1.setText("");
                    //puts all of the win statements within display
                    display = display +"\n" + introLetters + "    " + correctCounter 
                    + " correct, " + wrongCounter + " incorrect" + "\nYou won in "
                    + roundCounter + " rounds!" + "\nDo you wish to play again?";
                    //prints out display
                    jTextArea1.setText(display);
                    //you aren't playing anymore so gamePlaying is false
                    gamePlaying = false;
                    //clears the label
                    jLabel5.setText("");
                }
            //if guess isn't r,g or b and the proper length
            }else{
                //displays error message
                jLabel5.setText("Please enter a valid guess");
                //clears field
                jTextField1.setText("");
            }
            //if there is an error of index out of bounds
            }catch(IndexOutOfBoundsException e){
                //displays error message
                jLabel5.setText("Please enter a valid guess");
                //clears field
                jTextField1.setText("");
            }
    }
    }//GEN-LAST:event_jButton1ActionPerformed
